# take a vector of inputs and return a binary 
# matrix corresponding to the character
read_letter <- function(...) {
  cbind(
    matrix(data = c(...), nrow = 9, ncol = 5, byrow = TRUE),
    matrix(data = rep(0,9), nrow = 9, ncol = 1)
  )
}

